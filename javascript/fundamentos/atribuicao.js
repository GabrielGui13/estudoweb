const a = 7 //ja fez a primeira atribuicao, colocou o valor 7 dentro da const
let b = 3 //outra atribuicao

b += a // b = b + a
console.log(b) // 10 => 3 + 7 

b -= 4 // b = b - 4
console.log(b) // 6 => 10 - 4

b *= 2 // b = b * 2
console.log(b) // 12 => 6 * 2

b /= 2 //b = b / 2
console.log(b) // 6 => 12 / 2

b %= 2 // b = b % 2 => imprime o resto da divisao, se for par eh 0, se for impar eh 1 (dividido por 2)

